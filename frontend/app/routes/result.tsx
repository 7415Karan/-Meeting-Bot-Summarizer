import { useEffect, useState } from "react";
import { useNavigate } from "react-router";

export default function Result() {
  const [result, setResult] = useState<any>(null);
  const navigate = useNavigate();

  useEffect(() => {
    const stored = localStorage.getItem("meetingResult");
    if (stored) {
      try {
        setResult(JSON.parse(stored));
      } catch (e) {
        console.error("Failed to parse result", e);
      }
    }
  }, []);

  if (!result) {
    return (
      <div className="min-h-screen flex items-center justify-center bg-gray-100">
        <div className="text-center">
          <p className="text-gray-500 mb-4 text-lg">No result found.</p>
          <button
            onClick={() => navigate("/")}
            className="text-indigo-600 hover:underline font-medium"
          >
            &larr; Go back home
          </button>
        </div>
      </div>
    );
  }

  return (
    <div className="min-h-screen bg-gray-50 py-12 px-4 sm:px-6 lg:px-8">
      <div className="max-w-4xl mx-auto bg-white rounded-xl shadow-lg overflow-hidden border border-gray-200">
        {/* Header */}
        <div className="bg-indigo-600 px-8 py-6 flex justify-between items-center">
          <div>
            <h2 className="text-2xl font-bold text-white">Meeting Report</h2>
            <p className="text-indigo-100 text-sm mt-1">Generated by AI Assistant</p>
          </div>
          <div className="flex gap-2">
            <button
                onClick={() => navigate("/history")}
                className="text-white bg-indigo-500 hover:bg-indigo-400 px-4 py-2 rounded-md text-sm font-medium transition"
            >
                History
            </button>
            <button
                onClick={() => navigate("/")}
                className="text-indigo-600 bg-white hover:bg-gray-100 px-4 py-2 rounded-md text-sm font-medium transition"
            >
                New Meeting
            </button>
          </div>
        </div>

        <div className="p-8 space-y-8">
          {/* Executive Summary */}
          <section className="bg-gray-50 p-6 rounded-lg border border-gray-100">
            <h3 className="text-lg font-bold text-gray-900 mb-3 flex items-center">
              <span className="bg-indigo-100 text-indigo-600 p-1 rounded mr-2">üìù</span>
              Executive Summary
            </h3>
            <p className="text-gray-700 leading-relaxed text-base">
              {result.summary}
            </p>
          </section>

          <div className="grid md:grid-cols-2 gap-8">
            {/* Key Points */}
            <section>
              <h3 className="text-lg font-bold text-gray-900 mb-4 border-b pb-2">
                Key Discussion Points
              </h3>
              <ul className="space-y-3">
                {result.key_points?.map((k: string, i: number) => (
                  <li key={i} className="flex items-start text-gray-700">
                    <span className="flex-shrink-0 h-6 w-6 flex items-center justify-center rounded-full bg-blue-100 text-blue-600 text-xs font-bold mr-3 mt-0.5">
                      {i + 1}
                    </span>
                    <span>{k}</span>
                  </li>
                ))}
              </ul>
            </section>

            {/* Decisions */}
            <section>
              <h3 className="text-lg font-bold text-gray-900 mb-4 border-b pb-2">
                Decisions Made
              </h3>
              <ul className="space-y-3">
                {result.decisions?.length > 0 ? (
                  result.decisions.map((d: string, i: number) => (
                    <li key={i} className="flex items-start text-gray-700">
                      <span className="flex-shrink-0 h-6 w-6 flex items-center justify-center rounded-full bg-green-100 text-green-600 text-xs mr-3 mt-0.5">
                        ‚úì
                      </span>
                      <span className="font-medium">{d}</span>
                    </li>
                  ))
                ) : (
                  <li className="text-gray-500 italic">No explicit decisions recorded.</li>
                )}
              </ul>
            </section>
          </div>

          {/* Action Items Table */}
          {result.action_items && result.action_items.length > 0 && (
            <section className="mt-8">
              <h3 className="text-lg font-bold text-gray-900 mb-4">Action Items</h3>
              <div className="overflow-hidden shadow ring-1 ring-black ring-opacity-5 md:rounded-lg">
                <table className="min-w-full divide-y divide-gray-300">
                  <thead className="bg-gray-50">
                    <tr>
                      <th scope="col" className="py-3.5 pl-4 pr-3 text-left text-sm font-semibold text-gray-900 sm:pl-6">Task</th>
                      <th scope="col" className="px-3 py-3.5 text-left text-sm font-semibold text-gray-900">Owner</th>
                      <th scope="col" className="px-3 py-3.5 text-left text-sm font-semibold text-gray-900">Due Date</th>
                    </tr>
                  </thead>
                  <tbody className="divide-y divide-gray-200 bg-white">
                    {result.action_items.map((item: any, idx: number) => (
                      <tr key={idx} className="hover:bg-gray-50">
                        <td className="whitespace-normal py-4 pl-4 pr-3 text-sm font-medium text-gray-900 sm:pl-6">
                          {item.task}
                        </td>
                        <td className="whitespace-nowrap px-3 py-4 text-sm text-gray-500">
                          {item.owner ? (
                            <span className="inline-flex items-center rounded-md bg-purple-50 px-2 py-1 text-xs font-medium text-purple-700 ring-1 ring-inset ring-purple-700/10">
                              {item.owner}
                            </span>
                          ) : (
                            "‚Äî"
                          )}
                        </td>
                        <td className="whitespace-nowrap px-3 py-4 text-sm text-gray-500">
                          {item.due_date || "‚Äî"}
                        </td>
                      </tr>
                    ))}
                  </tbody>
                </table>
              </div>
            </section>
          )}
        </div>
      </div>
    </div>
  );
}
